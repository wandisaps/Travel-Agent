    public void register() throws SQLDataException, SQLException {
        try {
            if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || jComboBox1.getSelectedItem().equals("")
                    || jComboBox2.getSelectedItem().equals("") || jComboBox3.getSelectedItem().equals("") || jTextField3.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong", "Pesan", JOptionPane.ERROR_MESSAGE);
                hapuslayar();
            } else {
                String sql = "INSERT INTO user VALUES "
                        + "(0,'"+ jTextField1.getText() + "',"
                        + jTextField2.getText() + ",'"
                        + jComboBox1.getSelectedItem() + "-"
                        + jComboBox2.getSelectedItem() + "-"
                        + jComboBox3.getSelectedItem() + "',"
                        + "'" + jTextField4.getText() +"',"
                        + "'"+jPasswordField1.getText()+"',"
                        + "'"+ jTextField3.getText() + "','user')";
                java.sql.Connection conn = Koneksi.configDB();
                java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.execute();
                JOptionPane.showMessageDialog(null, "Registrasi Berhasil");
                login lg = new login();
                lg.show();
                this.dispose();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

